package com.thinh.entily4;

public class MyMath {

	public int sum(int a, int b) {
		return a + b;
	}

public byte sum(byte a, int b) {
	return (byte) (a+b);
}
public float sum(float a, int b) {
	return (float) (a +b);
}
public double sum(double a, byte b) {
	return (double) (a+b);
}
}

